    private void mySetting() {
        String[] columnNames = {"ID", "Name"};
        MyModel model = new MyModel(columnNames);
        
        for (Supplier s : suppliers) {
            String[] row = {"" + s.getSupplierId(), s.getName()};
            model.addRow(row);
        }
        jTable1.setModel(model);
    }
    
